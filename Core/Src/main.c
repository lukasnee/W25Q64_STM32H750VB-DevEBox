uint32_t crc32(const void *buf, size_t size)
{
    const uint8_t *p = buf;
    uint32_t crc;

    crc = ~0U;
    while (size--) {
        crc = crc32_tab[(crc ^ *p++) & 0xFF] ^ (crc >> 8);
    }
    return crc ^ ~0U;
}

void jump_to_app(const uint32_t address)
{
    __disable_irq();
    SysTick->CTRL = 0;
    SysTick->LOAD = 0;
    SysTick->VAL = 0;
    SCB->VTOR = address;
    vtor_t *app_vtor = (vtor_t *)address;
    /* Jump, used asm to avoid stack optimization */
    asm("msr msp, %0; bx %1;"
        :
        : "r"(app_vtor->initial_sp), "r"(app_vtor->initial_pc));
}

